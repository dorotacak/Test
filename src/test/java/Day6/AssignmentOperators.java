package Day6;

public class AssignmentOperators {
    public static void main(String[] args) {
        // Assignmenet                      Meaning
        // Addition        x+=y              x=x+y
        // Subtraction     x-=y              x=x-y

        int i=100;
        i+=1;  // same as i=i+1
        System.out.println(i);
        i-=2;   // same as i=i-2
        System.out.println(i);
        i*=3;   // same as i=i*3
        System.out.println(i);
        i/=3;   // same as i=i/3
        System.out.println(i);
        i%=5;   // same as i=i%5
        System.out.println(i);

    }
}
