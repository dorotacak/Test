package Day4;

public class AdditionConcat {
    public static void main(String[] args) {
        //number+ number
        System.out.println(2+2);
        //number+String
        System.out.println(2+"Dorota");
        // String+Boolean
        System.out.println("Dorota"+true);
        //String+String
        System.out.println("Dorota"+ "Senem");
        //examples
        System.out.println(2+0+3+"Dorota");
        System.out.println(10-5-9+"Dorota");
        System.out.println("Dorota"+2+0+5);
        System.out.println(2+0+5+"Dorota"+2+0+5);
        System.out.println(2+0+5+"Dorota"+(2+0+1));

    }
}
